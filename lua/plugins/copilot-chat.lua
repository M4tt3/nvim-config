return {
    {
        "CopilotC-Nvim/CopilotChat.nvim",
        dependencies = {
            { "nvim-lua/plenary.nvim", branch = "master" },
        },
        build = "make tiktoken",
        opts = {
            model = 'gpt-4.1',           -- AI model to use
            temperature = 0.1,           -- Lower = focused, higher = creative
            window = {
                layout = 'vertical',       -- 'vertical', 'horizontal', 'float'
                width = 0.4,              -- 50% of screen width
            },
            auto_insert_mode = false,     -- Enter insert mode when opening     -- See Configuration section for options
        },
        config = {
            -- Open chat and move to far right
            vim.keymap.set('n', '<leader>cc', '<cmd>CopilotChat<cr><c-w>L', { desc = 'Open Copilot Chat' }),

            -- Set buffer settings for Copilot Chat
            vim.api.nvim_create_autocmd('BufEnter', {
                pattern = 'copilot-*',
                callback = function()
                    vim.opt_local.relativenumber = false
                    vim.opt_local.number = false
                    vim.opt_local.conceallevel = 0
                end
            })
        }
    },
}
